package com.github.hayarobi.simple_config.sample;

public class NodeInfo {
	private String name;
	private String address;
	private int maxConn;
	
	/**
	 * @return the name
	 */
	public String getName() {
		return name;
	}
	/**
	 * @return the address
	 */
	public String getAddress() {
		return address;
	}
	/**
	 * @return the maxConn
	 */
	public int getMaxConn() {
		return maxConn;
	}
	
	
}
