package com.github.hayarobi.simple_config.sample;

public class PojoSub1 {
	private String host;
	private int port;
	private boolean keepalive;
	
	/**
	 * @return the host
	 */
	public String getHost() {
		return host;
	}
	/**
	 * @return the port
	 */
	public int getPort() {
		return port;
	}
	/**
	 * @return the keepalive
	 */
	public boolean isKeepalive() {
		return keepalive;
	}
	
	
}
